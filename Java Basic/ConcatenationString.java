public class ConcatenationString{
    public static void main(String[] args){
        String a = "Rahul";
        String b = "Aryal";

        String c = a +" "+ b;
        System.out.println(c);

        String e = "Hey ";
        String f = "Buddy";
        String d = e.concat(f);
        System.out.println(d);

    }
}




